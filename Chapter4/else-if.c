#include <stdio.h>

void main (void)
{
	// Declare the variable a
	int a = 0;
	
	// Test to see if a is equal to zero
	if (a == 0)
	{
		// Output the results if true
		printf ("a is equal to 0\n");
	}
	// If not, see if a is equal to one
	else if (a == 1)
	{
		// Output the results if true
		printf ("a is equal to 1\n");
	}
	// If not, display the default
	else
	{
		// Output the results if true
		printf ("a is greater than 1\n");
	}
}
